// scripts/build-sidebar-map.js
// Walks content/docs/ and builds a slug → sidebar-name lookup from each
// file's `displayed_sidebar` frontmatter, writing the result as a static
// TypeScript module that client components can import.
//
// Pages without `displayed_sidebar` default to 'main', which
// getSidebarForPathname treats as the fallback to config/sidebar.ts's
// `sidebars.main` (or the legacy `mainSidebar` export).
//
// Run via `npm run build-sidebar-map`, or automatically as part of
// `npm run dev` / `npm run build`.

const fs = require('fs');
const path = require('path');
const matter = require('gray-matter');

const ROOT = path.join(__dirname, '..');
const CONTENT_DIR = path.join(ROOT, 'content', 'docs');
const OUTPUT_FILE = path.join(ROOT, 'lib', 'sidebar-map.generated.ts');

function walk(dir, rel = '') {
  const out = [];
  if (!fs.existsSync(dir)) return out;
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    if (entry.name.startsWith('_') || entry.name.startsWith('.')) continue;
    const abs = path.join(dir, entry.name);
    const r = rel ? `${rel}/${entry.name}` : entry.name;
    if (entry.isDirectory()) out.push(...walk(abs, r));
    else if (/\.mdx?$/.test(entry.name)) out.push({ abs, rel: r });
  }
  return out;
}

function fileToSlug(rel) {
  return rel.replace(/\.mdx?$/, '').replace(/\/index$/, '');
}

function build() {
  const files = walk(CONTENT_DIR);
  const map = {};
  let assigned = 0;

  for (const { abs, rel } of files) {
    let data;
    try { ({ data } = matter(fs.readFileSync(abs, 'utf-8'))); } catch { continue; }
    if (!data || typeof data.displayed_sidebar !== 'string') continue;
    const slug = fileToSlug(rel);
    map[slug] = data.displayed_sidebar;
    assigned++;
  }

  const header = `// AUTO-GENERATED by scripts/build-sidebar-map.js — do not edit.\n`
    + `// Maps doc slugs (relative to content/docs/, without .mdx extension) to\n`
    + `// the \`displayed_sidebar\` frontmatter value. Consumed by getSidebarForPathname.\n\n`;
  const body = `export const sidebarAssignments: Record<string, string> = ${JSON.stringify(map, null, 2)}\n`;

  fs.mkdirSync(path.dirname(OUTPUT_FILE), { recursive: true });
  fs.writeFileSync(OUTPUT_FILE, header + body);

  console.log(`Sidebar map written: ${path.relative(ROOT, OUTPUT_FILE)} (${assigned} page(s) with displayed_sidebar)`);
}

build();
